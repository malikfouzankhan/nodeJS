// Protocols
/*
What is HTTP?
HTTP = HyperText Transfer Protocol
It is the rules and system that allow web browsers and servers to talk to each other.

Think of it like a language between:
    - your browser (client)
    - website server

Example:
When you type google.com, your browser sends an HTTP request -> server sends an HTTP response.


How HTTP works?
    1. You enter a URL
    2. Browser sends a request
    3. Server receives it
    4. Server processes it
    5. Server sends a response back
    6.Browser shows the result
*/

